class World(object):
    def __init__(self, carla_world, hud, actor_filter):
        self.world = carla_world
        self.hud = hud
        self.player = None
        self.collision_sensor = None
        self.lane_invasion_sensor = None
        self.gnss_sensor = None
        self.camera_manager = None
        self._weather_presets = find_weather_presets()
        self._weather_index = 0
        self._actor_filter = 'vehicle.audi.A2'
        self.restart()
        self.world.on_tick(hud.on_world_tick)
    
    def get_front_vehicle_distance(self):
        """
        Calculate the distance to the vehicle ahead.
        """
        ego_vehicle = self.player
        ego_location = ego_vehicle.get_transform().location
        ego_transform = ego_vehicle.get_transform()
        ego_waypoint = self.world.get_map().get_waypoint(ego_location)
        ego_lane_id = ego_waypoint.lane_id
        vehicles = self.world.get_actors().filter('vehicle.*')
        front_vehicle = None
        min_distance = float('inf')

        for vehicle in vehicles:
            if vehicle.id == ego_vehicle.id: 
                continue
            vehicle_location = vehicle.get_transform().location
            vehicle_waypoint = self.world.get_map().get_waypoint(vehicle_location)
            vehicle_lane_id = vehicle_waypoint.lane_id

            if ego_lane_id != vehicle_lane_id:
                continue
            relative_location = vehicle_location - ego_location
            if relative_location.dot(ego_transform.get_forward_vector()) > 0 and relative_location.length() < 50: 
                distance = relative_location.length()
                if distance < min_distance:
                    min_distance = distance
                    front_vehicle = vehicle
        return front_vehicle, min_distance if front_vehicle else None

    def restart(self):
        # Keep same camera config if the camera manager exists.
        cam_index = self.camera_manager.index if self.camera_manager is not None else 0
        cam_pos_index = self.camera_manager.transform_index if self.camera_manager is not None else 0
        # Get a random blueprint.
        blueprint = random.choice(self.world.get_blueprint_library().filter(self._actor_filter))
        blueprint.set_attribute('role_name', 'hero')
        if blueprint.has_attribute('color'):
            color = random.choice(blueprint.get_attribute('color').recommended_values)
            blueprint.set_attribute('color', color)
        # Spawn the player.
        if self.player is not None:
            spawn_point = self.player.get_transform()
            spawn_point.location.z += 2.0
            spawn_point.rotation.roll = 0.0
            spawn_point.rotation.pitch = 0.0
            self.destroy()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
        while self.player is None:
            spawn_points = self.world.get_map().get_spawn_points()
            spawn_point = random.choice(spawn_points) if spawn_points else carla.Transform()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
        # Set up the sensors.
        self.collision_sensor = CollisionSensor(self.player, self.hud)
        self.lane_invasion_sensor = LaneInvasionSensor(self.player, self.hud)
        self.gnss_sensor = GnssSensor(self.player)
        self.camera_manager = CameraManager(self.player, self.hud)
        self.camera_manager.transform_index = cam_pos_index
        self.camera_manager.set_sensor(cam_index, notify=False)
        actor_type = get_actor_display_name(self.player)
        self.hud.notification(actor_type)

    def next_weather(self, reverse=False):
        self._weather_index += -1 if reverse else 1
        self._weather_index %= len(self._weather_presets)
        preset = self._weather_presets[self._weather_index]
        self.hud.notification('Weather: %s' % preset[1])
        self.player.get_world().set_weather(preset[0])

    def tick(self, clock):
        self.hud.tick(self, clock)

    def render(self, display):
        self.camera_manager.render(display)
        self.hud.render(display)

    def destroy(self):
        sensors = [
            self.camera_manager.sensor,
            self.collision_sensor.sensor,
            self.lane_invasion_sensor.sensor,
            self.gnss_sensor.sensor]
        for sensor in sensors:
            if sensor is not None:
                sensor.stop()
                sensor.destroy()
        if self.player is not None:
            self.player.destroy()
    def is_driving_against_traffic(self):
        """
        Determine whether it is reverse.
        """
        ego_vehicle = self.player
        if not ego_vehicle:
            return False
        ego_location = ego_vehicle.get_transform().location
        ego_forward_vector = ego_vehicle.get_transform().get_forward_vector()
        waypoint = self.world.get_map().get_waypoint(ego_location)
        lane_forward_vector = waypoint.transform.get_forward_vector()
        dot_product = (
            ego_forward_vector.x * lane_forward_vector.x +
            ego_forward_vector.y * lane_forward_vector.y +
            ego_forward_vector.z * lane_forward_vector.z
        )
        magnitude_ego = math.sqrt(
            ego_forward_vector.x**2 + ego_forward_vector.y**2 + ego_forward_vector.z**2)
        magnitude_lane = math.sqrt(
            lane_forward_vector.x**2 + lane_forward_vector.y**2 + lane_forward_vector.z**2)
        angle = math.acos(dot_product / (magnitude_ego * magnitude_lane)) 
        return angle > math.pi / 2

    def get_lane_offset(self):
        """
        Calculates the vehicle's offset distance from the center of the lane.
        """
        ego_vehicle = self.player
        ego_location = ego_vehicle.get_transform().location
        waypoint = self.world.get_map().get_waypoint(ego_location)
        lane_center = waypoint.transform.location
        offset_vector = ego_location - lane_center
        offset_distance = math.sqrt(offset_vector.x**2 + offset_vector.y**2)
        forward_vector = ego_vehicle.get_transform().get_forward_vector()
        right_vector = carla.Vector3D(-forward_vector.y, forward_vector.x, 0)
        signed_offset = offset_vector.dot(right_vector)
        return signed_offset

